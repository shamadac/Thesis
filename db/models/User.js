const { Schema } = require('mongoose')

module.exports =  new Schema(
  {
    firstname: {
      type: String,
      required: true
    },
    lastname: {
      type: String,
      required: true
    },
    username: {
      type: String,
      unique: true,
      required: true
    },
    password: {
      type: String,
      required: true
    },
    email: {
      type: String,
      required: true
    },
    phone: {
      type: String,
      required: true
    },
    credentials: [ Schema.Types.Mixed ],
    communities: [ Schema.Types.Mixed ]
  },
  {
    timestamps: true
  }
)